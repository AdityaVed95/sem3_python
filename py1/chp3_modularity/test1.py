def main():
    print("Hello you are in the main function")

def fxn1():
    print("Hello you are in fxn1")

def fxn2():
    print("Hello you are in fxn2")

def fxn3():
    print("Hello you are in fxn3")

fxn1()

# here we learn that it is not always necessary that the main function will run in a .py file
# unless explicitly called , the main fxn wont run
# this is not like java or C where all the execution begins from the main function and by default the main
# function is run at the start

